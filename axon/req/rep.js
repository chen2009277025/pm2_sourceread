var axon = require('axon');
var sock = axon.socket('rep');

sock.connect(3000);

sock.on('message', function(task, username, reply){
  // resize the image
  switch (task){
    case 'hello world':
      reply('hello '+ username);
      break;
    case 'bye bye':
      reply('滚:'+username);
      break;
  }
});